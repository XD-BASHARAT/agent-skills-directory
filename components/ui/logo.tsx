function Logo({ name, size = "1em", className, fill, fillRule, ...props }: LogoProps) {
  const imageLogo = logoImages[name]
  const logoData = logoPaths[name]
  const title = logoTitles[name]

  if (imageLogo) {
    const fallbackSize = typeof size === "number" ? size : 16
    const dimensionStyle = typeof size === "number" ? `${size}px` : size
    return (
      <Image
        src={imageLogo.src}
        alt={title}
        width={fallbackSize}
        height={fallbackSize}
        className={cn("shrink-0", className)}
        style={{ height: dimensionStyle, width: dimensionStyle }}
      />
    )
  }

  return (
    <svg
      fill={fill ?? logoData.fill ?? "currentColor"}
      fillRule={fillRule ?? "evenodd"}
      height={size}
      width={size}
      style={{ flex: "none", lineHeight: 1 }}
      viewBox="0 0 24 24"
      xmlns="http://www.w3.org/2000/svg"
      className={cn("shrink-0", className)}
      aria-label={title}
      role="img"
      {...props}
    >
      <title>{title}</title>
      {logoData.paths}
    </svg>
  )
}

